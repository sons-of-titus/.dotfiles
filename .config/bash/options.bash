# Shell Options

# Check window size after each command
shopt -s checkwinsize

# Enable extended globbing
shopt -s extglob
shopt -s globstar

# Correct minor spelling errors in directory names
shopt -s cdspell

# Autocorrect directory names during completion
shopt -s dirspell

# Enable ** globbing
shopt -s globstar

# Append to history file instead of overwriting
shopt -s histappend

# Save multi-line commands as single line
shopt -s cmdhist

# Enable case-insensitive filename globbing
shopt -s nocaseglob
